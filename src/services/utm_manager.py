import json
import os
from typing import Dict, List, Tuple
import logging

logger = logging.getLogger(__name__)

class UTMManager:
    def __init__(self, data_file: str = "data/utm_data.json"):
        self.data_file = data_file
        self.data_dir = os.path.dirname(data_file)
        self.ensure_data_file_exists()
        self.load_data()

    def ensure_data_file_exists(self):
        """–°–æ–∑–¥–∞–µ—Ç –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –∏ —Ñ–∞–π–ª –¥–∞–Ω–Ω—ã—Ö, –µ—Å–ª–∏ –æ–Ω–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É—é—Ç"""
        try:
            os.makedirs(self.data_dir, exist_ok=True)
            if not os.path.exists(self.data_file):
                initial_data = {
                    "sources": [],
                    "mediums": {
                        "publications": [],
                        "mailings": [],
                        "stories": [],
                        "channels": []
                    },
                    "campaigns": {
                        "spb": [],
                        "msk": [],
                        "tr": [],
                        "regions": [],
                        "foreign": []
                    }
                }
                with open(self.data_file, 'w', encoding='utf-8') as f:
                    json.dump(initial_data, f, ensure_ascii=False, indent=2)
        except Exception as e:
            logger.error(f"Error creating data file: {e}")

    def load_data(self):
        """–ó–∞–≥—Ä—É–∂–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –∏–∑ JSON —Ñ–∞–π–ª–∞"""
        try:
            with open(self.data_file, 'r', encoding='utf-8') as f:
                self.data = json.load(f)
        except Exception as e:
            logger.error(f"Error loading data: {e}")
            self.data = {
                "sources": [],
                "mediums": {"publications": [], "mailings": [], "stories": [], "channels": []},
                "campaigns": {"spb": [], "msk": [], "tr": [], "regions": [], "foreign": []}
            }

    def save_data(self):
        """–°–æ—Ö—Ä–∞–Ω—è–µ—Ç –¥–∞–Ω–Ω—ã–µ –≤ JSON —Ñ–∞–π–ª"""
        try:
            with open(self.data_file, 'w', encoding='utf-8') as f:
                json.dump(self.data, f, ensure_ascii=False, indent=2)
            return True
        except Exception as e:
            logger.error(f"Error saving data: {e}")
            return False

    def get_all_categories(self) -> Dict:
        """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≤—Å–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –¥–ª—è –∏–Ω–ª–∞–π–Ω-–∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã"""
        return {
            "utm_source": ("üìä –ò—Å—Ç–æ—á–Ω–∏–∫–∏ —Ç—Ä–∞—Ñ–∏–∫–∞ (utm_source)", "source"),
            "utm_medium_publications": ("üì£ –ü—É–±–ª–∏–∫–∞—Ü–∏–∏ (utm_medium)", "medium_publications"),
            "utm_medium_mailings": ("üìß –†–∞—Å—Å—ã–ª–∫–∏ (utm_medium)", "medium_mailings"),
            "utm_medium_stories": ("üì± –°—Ç–æ—Ä–∏—Å—ã (utm_medium)", "medium_stories"),
            "utm_medium_channels": ("üì° –ö–∞–Ω–∞–ª—ã (utm_medium)", "medium_channels"),
            "utm_campaign_spb": ("üìç –°–ü–ë –∫–∞–º–ø–∞–Ω–∏–∏", "campaign_spb"),
            "utm_campaign_msk": ("üèô –ú–°–ö –∫–∞–º–ø–∞–Ω–∏–∏", "campaign_msk"),
            "utm_campaign_tr": ("‚úàÔ∏è –¢—É—Ä—Ü–∏—è –∫–∞–º–ø–∞–Ω–∏–∏", "campaign_tr"),
            "utm_campaign_regions": ("üåç –†–µ–≥–∏–æ–Ω—ã –∫–∞–º–ø–∞–Ω–∏–∏", "campaign_regions"),
            "utm_campaign_foreign": ("üåê –ó–∞—Ä—É–±–µ–∂—å–µ –∫–∞–º–ø–∞–Ω–∏–∏", "campaign_foreign")
        }

    def get_category_data(self, category_key: str) -> List[Tuple[str, str]]:
        """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –¥–ª—è –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏"""
        category_map = {
            "source": ("sources", None),
            "medium_publications": ("mediums", "publications"),
            "medium_mailings": ("mediums", "mailings"),
            "medium_stories": ("mediums", "stories"),
            "medium_channels": ("mediums", "channels"),
            "campaign_spb": ("campaigns", "spb"),
            "campaign_msk": ("campaigns", "msk"),
            "campaign_tr": ("campaigns", "tr"),
            "campaign_regions": ("campaigns", "regions"),
            "campaign_foreign": ("campaigns", "foreign")
        }
        
        if category_key in category_map:
            main_key, sub_key = category_map[category_key]
            if sub_key:
                return self.data[main_key][sub_key]
            else:
                return self.data[main_key]
        return []

    def add_item(self, category_key: str, name: str, value: str) -> bool:
        """–î–æ–±–∞–≤–ª—è–µ—Ç –Ω–æ–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç –≤ –∫–∞—Ç–µ–≥–æ—Ä–∏—é"""
        try:
            category_map = {
                "source": ("sources", None),
                "medium_publications": ("mediums", "publications"),
                "medium_mailings": ("mediums", "mailings"),
                "medium_stories": ("mediums", "stories"),
                "medium_channels": ("mediums", "channels"),
                "campaign_spb": ("campaigns", "spb"),
                "campaign_msk": ("campaigns", "msk"),
                "campaign_tr": ("campaigns", "tr"),
                "campaign_regions": ("campaigns", "regions"),
                "campaign_foreign": ("campaigns", "foreign")
            }
            
            if category_key not in category_map:
                return False
            
            main_key, sub_key = category_map[category_key]
            item = [name, value]
            
            if sub_key:
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞ –¥—É–±–ª–∏–∫–∞—Ç—ã
                existing_items = self.data[main_key][sub_key]
                if any(existing_item[1] == value for existing_item in existing_items):
                    return False
                self.data[main_key][sub_key].append(item)
            else:
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞ –¥—É–±–ª–∏–∫–∞—Ç—ã
                if any(existing_item[1] == value for existing_item in self.data[main_key]):
                    return False
                self.data[main_key].append(item)
            
            return self.save_data()
        except Exception as e:
            logger.error(f"Error adding item: {e}")
            return False

    def delete_item(self, category_key: str, value: str) -> bool:
        """–£–¥–∞–ª—è–µ—Ç —ç–ª–µ–º–µ–Ω—Ç –∏–∑ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏"""
        try:
            category_map = {
                "source": ("sources", None),
                "medium_publications": ("mediums", "publications"),
                "medium_mailings": ("mediums", "mailings"),
                "medium_stories": ("mediums", "stories"),
                "medium_channels": ("mediums", "channels"),
                "campaign_spb": ("campaigns", "spb"),
                "campaign_msk": ("campaigns", "msk"),
                "campaign_tr": ("campaigns", "tr"),
                "campaign_regions": ("campaigns", "regions"),
                "campaign_foreign": ("campaigns", "foreign")
            }
            
            if category_key not in category_map:
                return False
            
            main_key, sub_key = category_map[category_key]
            
            if sub_key:
                self.data[main_key][sub_key] = [
                    item for item in self.data[main_key][sub_key] 
                    if item[1] != value
                ]
            else:
                self.data[main_key] = [
                    item for item in self.data[main_key] 
                    if item[1] != value
                ]
            
            return self.save_data()
        except Exception as e:
            logger.error(f"Error deleting item: {e}")
            return False

